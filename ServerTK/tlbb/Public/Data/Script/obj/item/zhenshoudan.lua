	local petItem = x300027_g_petList[itemTblIndex];
	if not petItem then
		x300027_NotifyFailTips(sceneId, selfId, "Chßa m· ğÕo cø, không th¬ sØ døng. ");
		return 0;
	end

	local petGUID_H = 0
