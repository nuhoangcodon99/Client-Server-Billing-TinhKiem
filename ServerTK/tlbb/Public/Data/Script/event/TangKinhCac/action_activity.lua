--**********************************--
--*        On Default Event        *--
--**********************************--
function x910126_OnDefaultEvent(sceneId,actId,iNoticeType,param2,param3,param4,param5)

	--******************--
	StartOneActivity(sceneId,actId,floor(60*1000),iNoticeType)
	--******************--
	for i,v in x910126_g_NPC_Pos do
		if sceneId==v.SceneId then
			x910126_CreatMyNPC(sceneId,i)
		end
	end
	--******************--

end
--**********************************--
--*            On Timer            *--
--**********************************--
function x910126_OnTimer(sceneId,actId,uTime)

	--******************--
	if CheckActiviyValidity(sceneId,actId)==0 then
		StopOneActivity(sceneId,actId)
	end
	--******************--

end
--**********************************--
--*          Creat My NPC          *--
--**********************************--
function x910126_CreatMyNPC(sceneId,i)

	--******************--
	local MstId=LuaFnCreateMonster(sceneId,x910126_g_NPC.ID,x910126_g_NPC_Pos[i].Pos_X,x910126_g_NPC_Pos[i].Pos_Y,3,0,x910126_g_NPC.ScriptID)
	SetCharacterName(sceneId,MstId,x910126_g_NPC.Name)
	SetCharacterDieTime(sceneId,MstId,1800000)
	--******************--

end
