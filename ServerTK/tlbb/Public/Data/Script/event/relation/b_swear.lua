	--(1)ÊÇ·ñ×é¶Ó
	if LuaFnHasTeam( sceneId, selfId ) == 0 then
		x806001_MessageBox( sceneId, selfId, targetId, x806001_g_msg_swear["tem"] )
		return 0
	end
	
	--È¡¶ÓÎéÈËÊý£¬ÈËÊý²»ÄÜÊÇ1¸ö
	local TeamSize = LuaFnGetTeamSize( sceneId, selfId )
