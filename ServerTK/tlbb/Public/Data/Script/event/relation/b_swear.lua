		--±éÀú¶ÓÔ±
		j = i
		while j < TeamSizeSameScene do
			otherID = LuaFnGetTeamSceneMember( sceneId, theID, j )
			if LuaFnIsSpouses(sceneId, theID, otherID) == 1 then
				x806001_MessageBox( sceneId, selfId, targetId, x806001_g_msg_swear["mar"] )
