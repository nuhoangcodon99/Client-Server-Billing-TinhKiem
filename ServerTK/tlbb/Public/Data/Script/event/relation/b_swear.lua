			return 0
		end

		--ÊÇ·ñÒÑ¾­ÓÐ½á°Ý¹ØÏµ
		local theSwear = LuaFnIsSweared(sceneId, theID)
		if theSwear > 0 then --¶ÓÔ±ÓÐ½á°Ý¹ØÏµ
			if BrotherNum > 0 then --¶Ó³¤ÓÐ½á°Ý¹ØÏµ
				if LuaFnIsBrother(sceneId, selfId, theID) == 0 then
					local	theName	= LuaFnGetName( sceneId, theID )
