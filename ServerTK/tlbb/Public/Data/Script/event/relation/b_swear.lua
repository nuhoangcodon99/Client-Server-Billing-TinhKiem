	local Friend1 = 0
	local Friend2 = 0
	local Brothers = 0
	local BrotherNum = LuaFnIsSweared(sceneId, selfId) --¶Ó³¤ÊÇ·ñ½á°Ý¹ý
	while i < TeamSizeSameScene do
		theID = LuaFnGetTeamSceneMember( sceneId, selfId, i )

