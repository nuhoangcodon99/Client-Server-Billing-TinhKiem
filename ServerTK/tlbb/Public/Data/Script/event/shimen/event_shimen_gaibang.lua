function x229008_HelpFinishOneHuan( sceneId, selfId, targetId )

	-- «∑Ò «±æ√≈≈…µ‹◊”....
	if MP_GAIBANG ~= GetMenPai( sceneId, selfId ) then
		return
	end

	-- «∑ÒΩ”¡À ¶√≈»ŒŒÒ....
	if IsHaveMission(sceneId,selfId,x229008_g_MissionId) <= 0 then
		return
	end

