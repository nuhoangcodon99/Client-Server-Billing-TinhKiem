		--Ω±¿¯ÕÊº““ª∏ˆ»•±µƒ√≈≈…∏±±æµƒ ¶√≈»ŒŒÒ....
		CallScriptFunction(229010, "AddOtherMenpaiFubenMission", sceneId, selfId, x229008_g_MissionId, targetId)
	end

end
