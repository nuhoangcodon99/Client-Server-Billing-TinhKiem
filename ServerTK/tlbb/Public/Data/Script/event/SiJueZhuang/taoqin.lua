function x910174_CheckMyPet(sceneId,selfId)

	--******************--
	local Bird,Tiger=0,0
	--******************--
	local nMonsterNum=GetMonsterCount(sceneId)
	for i=0,nMonsterNum-1 do
		local MonsterId=GetMonsterObjID(sceneId,i)
		if LuaFnIsCharacterLiving(sceneId,MonsterId)==1 and GetName(sceneId,MonsterId)==x910174_g_Pet[2].Name then
			Bird=1
		end
		if LuaFnIsCharacterLiving(sceneId,MonsterId)==1 and GetName(sceneId,MonsterId)==x910174_g_Pet[1].Name then
			Tiger=1
		end
	end
	--******************--
	if Tiger==0 then
		LuaFnCancelSpecificImpact(sceneId,selfId,x910174_g_KuangBao_Impact)
	end
	--******************--
	if Bird==0 then
		LuaFnCancelSpecificImpact(sceneId,selfId,x910174_g_Immue_Impact)
	end
	--******************--
	
end
