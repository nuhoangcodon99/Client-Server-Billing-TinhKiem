function ScriptGlobal_IsUniqueNumberTable( numTable )
	
	local isUnique = 1;
	
	if( numTable == nil ) then
		return 0;
	end
	
	local tableLen = getn(numTable)
	if(tableLen <= 0) then
		return 0;
	end
	
	for i = 1, tableLen do
		if numTable[i] ~= nil and numTable[i] >= 0  then
		    for j = 1, tableLen do
		        if i ~= j and numTable[i] == numTable[j] then
		            isUnique = 0;
		            return isUnique;
		        end
		    end
	    end
	end
	
	return isUnique;
	
end